def seed(db_path: str | None = None) -> int:
    """Load all seed applications into the database. Returns count."""
    conn = get_connection(db_path)
    count = 0
    for entry in SEED_APPLICATIONS:
        app = Application(**entry)
        upsert_application(conn, app)
        count += 1
    conn.close()
    return count


def status(db_path: str | None = None) -> None:
    """Print current database status."""
    conn = get_connection(db_path)
    total = conn.execute("SELECT COUNT(*) FROM application").fetchone()[0]
    print(f"Total applications: {total}")
    print()
    print("By domain:")
    for row in conn.execute(
        "SELECT domain, COUNT(*) FROM application GROUP BY domain ORDER BY COUNT(*) DESC"
    ).fetchall():
        print(f"  {row[0]:30s} {row[1]:>3d}")
    print()
    print("By advantage type:")
    for row in conn.execute(
        "SELECT advantage_type, COUNT(*) FROM application GROUP BY advantage_type ORDER BY COUNT(*) DESC"
    ).fetchall():
        print(f"  {row[0]:25s} {row[1]:>3d}")
    print()
    print("By advantage status:")
    for row in conn.execute(
        "SELECT advantage_status, COUNT(*) FROM application GROUP BY advantage_status ORDER BY COUNT(*) DESC"
    ).fetchall():
        print(f"  {row[0]:25s} {row[1]:>3d}")
    print()
    print("By maturity:")
    for row in conn.execute(
        "SELECT maturity, COUNT(*) FROM application GROUP BY maturity ORDER BY COUNT(*) DESC"
    ).fetchall():
        print(f"  {row[0]:25s} {row[1]:>3d}")
    conn.close()


if __name__ == "__main__":
    if "--status" in sys.argv:
        status()
    else:
        n = seed()
        print(f"Seeded {n} quantum computing applications.")
        status()
